package percabangan;

public class Switch {
    public static void main(String[] args) {
        int karakter=4;
        switch (karakter){
            case 1:
                System.out.println("Sorceress");
            case 2:
                System.out.println("Guardian");
            case 3:
                System.out.println("Mystic");
            case 4:
                System.out.println("Lahn");
            case 5:
                System.out.println("Berserker");
            default:
                System.out.println("Loli");
        }
    }
}
