package percabangan;

public class Switch {
    public static void main(String[] args) {
        int karakter=2;
        switch (karakter){
            case 1:
                System.out.println("Sorceress");
                break;
            case 2:
                System.out.println("Guardian");
                break;
            case 3:
                System.out.println("Mystic");
                break;
            case 4:
                System.out.println("Lahn");
                break;
            case 5:
                System.out.println("Berserker");
                break;
            default:
                System.out.println("Loli");
                break;
        }
    }
}
